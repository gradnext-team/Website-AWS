// Function to search colleges with fuzzy matching
export const searchColleges = (query, limit = 10) => {
  if (!query || query.length < 2) return [];
  
  const normalizedQuery = query.toLowerCase().trim();
  
  // Exact matches first, then partial matches
  const exactMatches = [];
  const startsWithMatches = [];
  const containsMatches = [];
  
  COLLEGES.forEach(college => {
    const normalizedCollege = college.toLowerCase();
    
    if (normalizedCollege === normalizedQuery) {
      exactMatches.push(college);
    } else if (normalizedCollege.startsWith(normalizedQuery)) {
      startsWithMatches.push(college);
    } else if (normalizedCollege.includes(normalizedQuery)) {
      containsMatches.push(college);
    }
  });
  
  // Combine results with priority
  const results = [...exactMatches, ...startsWithMatches, ...containsMatches];
  
  return results.slice(0, limit);
};

// Get all colleges (for dropdown if needed)
export const getAllColleges = () => COLLEGES;

// Check if a college is in the list
export const isKnownCollege = (college) => {
  if (!college) return false;
  const normalizedCollege = college.toLowerCase().trim();
  return COLLEGES.some(c => c.toLowerCase() === normalizedCollege);
};

